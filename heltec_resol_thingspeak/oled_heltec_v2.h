static char oledNormChar(char c) {
  if (c >= 'a' && c <= 'z') {
    c = (char)(c - 32);
  }
  if (c < 32 || c > 90) {
    return '?';
  }
  return c;
}

static void oledDrawChar(int x, int y, char c) {
  c = oledNormChar(c);
  const uint8_t* glyph = font5x7[c - 32];
  for (int col = 0; col < 5; col++) {
    uint8_t line = glyph[col];
    for (int row = 0; row < 7; row++) {
      if (line & (1 << row)) {
        int px = x + col;
        int py = y + row;
        if (px >= 0 && px < OLED_W && py >= 0 && py < OLED_H) {
          oledBuffer[px + (py / 8) * OLED_W] |= (1 << (py & 7));
        }
      }
    }
  }
}

static void oledDrawString(int x, int y, const char* text) {
  int cursor = x;
  while (*text) {
    oledDrawChar(cursor, y, *text++);
    cursor += 6;
  }
}

// Font compatto: stesso 5x7, interlinea 8 px → 6 righe su 64 px
static void oledDrawStringCompact(int x, int y, const char* text) {
  int cursor = x;
  while (*text) {
    oledDrawChar(cursor, y, *text++);
    cursor += 5;
  }
}

static void oledShowLines(const char* l1, const char* l2, const char* l3, const char* l4) {
  oledClear();
  oledDrawString(0, 0, l1);
  oledDrawString(0, 16, l2);
  oledDrawString(0, 32, l3);
  oledDrawString(0, 48, l4);
  oledPush();
}

static void oledShowLinesCompact(
  const char* l1, const char* l2, const char* l3,
  const char* l4, const char* l5, const char* l6) {
  oledClear();
  oledDrawStringCompact(0, 0, l1);
  oledDrawStringCompact(0, 8, l2);
  oledDrawStringCompact(0, 16, l3);
  oledDrawStringCompact(0, 24, l4);
  oledDrawStringCompact(0, 32, l5);
  oledDrawStringCompact(0, 40, l6);
  oledPush();
}

